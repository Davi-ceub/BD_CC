create database Videos;
use Videos;

create table conteudos (
id int primary key auto_increment unique,
titulo varchar(100) not null,
tipo varchar(50) not null,
genero varchar(50) not null,
ano_lancamento int not null,
duracao_minutos int not null,
classificacao_etaria int not null,
status_disponibilidade varchar(50) not null,
idiomas varchar(50) not null);

create table  usuarios (
id int primary key auto_increment,
nome varchar(100) not null,
email varchar(100) not null unique,
telefone bigint not null,
data_cadastro date not null,
plano_assinatura_id int not null,
contas int not null);

create table planos (
id int primary key auto_increment unique,
nome_plano varchar(75) not null, 
preco_mensal decimal(9,2) not null, 
resolucao_maxima varchar(10) not null, 
numero_telas int not null,
forma_pagamento varchar(50) not null);

create table historico (
id int primary key auto_increment unique,
id_usuario int not null, 
id_conteudo int not null, 
data_visualizacao date not null, 
progresso_percentual int not null,
hora_total int not null);

create table avaliacoes (
id int primary key auto_increment unique, 
id_usuario int not null, 
id_conteudo int not null, 
nota int not null, 
comentario varchar(100) not null, 
data_avaliacao date not null,
qtd_avaliacoes int not null);

alter table usuarios add constraint fk_planos foreign key(plano_assinatura_id)
references planos (id);

alter table historico add constraint fk_usuarios foreign key(id_usuario)
references usuarios (id);

alter table historico add constraint fk_conteudos foreign key(id_conteudo)
references conteudos (id);

alter table avaliacoes add constraint fk_usuario foreign key(id_usuario)
references usuarios (id);

alter table avaliacoes add constraint fk_conteudo foreign key(id_conteudo)
references conteudos (id);

