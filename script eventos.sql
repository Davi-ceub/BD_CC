create database Eventos;
use Eventos;

create table eventos (
id int primary key auto_increment,
nome_evento varchar(100) not null unique,
descricao varchar(150) not null,
data_inicio date not null,
data_fim date not null,
local_e varchar(50) not null,
organizador_id int not null,
Tipo varchar(50) not null);

create table organizadores (
id int primary key auto_increment,
nome varchar(75) not null,
contato_email varchar(100) not null unique,
telefone Bigint not null,
cargo varchar(50) not null);

create table participantes (
id int primary key auto_increment,
nome varchar(100) not null,
email varchar(100) not null unique,
telefone Bigint not null,
data_inscricao Date not null,
forma_pagamento varchar(50) not null);

create table inscricoes (
id int primary key auto_increment,
id_evento int not null unique,
id_participante int not null,
data_inscricao date not null,
status_pagamento varchar(50) not null,
vagas int not null);

alter table eventos add constraint fk_organizadores foreign key (organizador_id)
references organizadores (id);

alter table inscricoes add constraint fk_eventos foreign key (id_evento)
references eventos (id);

alter table inscricoes add constraint fk_participantes foreign key (id_participante)
references participantes (id);

